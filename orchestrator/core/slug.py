"""Безопасный slug из отображаемого имени сессии.

Название топика может быть любым (пробелы, эмодзи, кириллица), но папка сессии,
ключ MCP-сервера (channel-<slug>), CLI-аргумент server:... и URL хука
/event/<slug> требуют [A-Za-z0-9_-]. Кириллица транслитерируется; если после
этого не осталось латиницы/цифр — автослаг. Вынесено из sessions.py (D1).
"""

from __future__ import annotations

import re
import uuid

# Транслитерация кириллицы в латиницу (ГОСТ-подобная, упрощённая).
TRANSLIT = {
    "а": "a", "б": "b", "в": "v", "г": "g", "д": "d", "е": "e", "ё": "e",
    "ж": "zh", "з": "z", "и": "i", "й": "j", "к": "k", "л": "l", "м": "m",
    "н": "n", "о": "o", "п": "p", "р": "r", "с": "s", "т": "t", "у": "u",
    "ф": "f", "х": "h", "ц": "c", "ч": "ch", "ш": "sh", "щ": "sch",
    "ъ": "", "ы": "y", "ь": "", "э": "e", "ю": "yu", "я": "ya",
}


def slugify(title: str) -> str:
    """Безопасный непустой идентификатор из отображаемого имени (см. модуль docstring)."""
    translit = "".join(TRANSLIT.get(ch, TRANSLIT.get(ch.lower(), ch)) for ch in title)
    slug = re.sub(r"[^A-Za-z0-9_-]+", "-", translit).strip("-")[:32]
    return slug or f"session-{uuid.uuid4().hex[:6]}"
